class Solution:
    # code help by mingyu
    def change(self, amount: int, coins: List[int]) -> int:
        d = [0] * (amount+1)
        d[0] = 1
        for coin in coins:
            for i in range(amount+1):
                if i+coin <= amount:
                    # add coin in advance combinations
                    d[i+coin] += d[i]
        return d[-1]

